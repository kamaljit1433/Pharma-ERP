/**
 * Download report as file
 */
export const downloadReport = (
  content: string,
  filename: string,
  format: 'html' | 'csv' = 'html'
): void => {
  const mimeType = format === 'html' ? 'text/html;charset=utf-8;' : 'text/csv;charset=utf-8;';
  const blob = new Blob([content], { type: mimeType });
  const url = window.URL.createObjectURL(blob);
  const link = document.createElement('a');
  link.href = url;
  link.setAttribute('download', filename);
  document.body.appendChild(link);
  link.click();
  document.body.removeChild(link);
  window.URL.revokeObjectURL(url);
};

/**
 * Print report directly
 */
export const printReport = (reportHTML: string, pageSize: 'A4' | 'Letter' = 'A4'): void => {
  const printWindow = window.open('', '', 'width=800,height=600');
  if (!printWindow) {
    console.error('Failed to open print window');
    return;
  }

  const fullHTML = createPrintDocument(reportHTML, pageSize);
  printWindow.document.write(fullHTML);
  printWindow.document.close();

  // Wait for content to load before printing
  setTimeout(() => {
    printWindow.print();
  }, 250);
};

/**
 * Generate report filename with timestamp
 */
export const generateReportFilename = (
  prefix: string,
  format: 'html' | 'csv' = 'html'
): string => {
  const timestamp = new Date().toISOString().split('T')[0];
  const ext = format === 'html' ? 'html' : 'csv';
  return `${prefix}-${timestamp}.${ext}`;
};
