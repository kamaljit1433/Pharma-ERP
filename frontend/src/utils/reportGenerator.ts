/**
 * Report Generator Utility
 * Formats data into printable reports with metadata
 * 
 * Requirements: 26.5, 26.6, 26.7, 26.8
 */

export interface ReportMetadata {
  title: string;
  description?: string;
  generatedDate: Date;
  generatedBy: string;
  filters?: Record<string, any>;
  pageSize?: 'A4' | 'Letter';
}

export interface ReportData {
  metadata: ReportMetadata;
  columns: ReportColumn[];
  rows: any[];
  summary?: ReportSummary;
}

export interface ReportColumn {
  id: string;
  label: string;
  width?: string;
  align?: 'left' | 'center' | 'right';
  format?: (value: any) => string;
}

export interface ReportSummary {
  label: string;
  value: string | number;
}[]

/**
 * Format metadata for display in report
 */
export const formatReportMetadata = (metadata: ReportMetadata): string => {
  const lines: string[] = [];

  lines.push(`Generated: ${metadata.generatedDate.toLocaleString()}`);
  lines.push(`Generated By: ${metadata.generatedBy}`);

  if (metadata.filters && Object.keys(metadata.filters).length > 0) {
    const filterStr = Object.entries(metadata.filters)
      .map(([key, value]) => {
        if (value === null || value === undefined) return null;
        if (Array.isArray(value)) {
          return `${key}: ${value.join(', ')}`;
        }
        return `${key}: ${value}`;
      })
      .filter(Boolean)
      .join(' | ');

    if (filterStr) {
      lines.push(`Filters: ${filterStr}`);
    }
  }

  return lines.join('\n');
};

/**
 * Format cell value based on column format function
 */
export const formatCellValue = (value: any, column: ReportColumn): string => {
  if (column.format) {
    return column.format(value);
  }

  if (value === null || value === undefined) {
    return '-';
  }

  if (value instanceof Date) {
    return value.toLocaleDateString();
  }

  if (typeof value === 'number') {
    return value.toLocaleString();
  }

  return String(value);
};

/**
 * Generate HTML table from report data
 */
export const generateReportHTML = (data: ReportData): string => {
  const { metadata, columns, rows, summary } = data;

  const headerRow = columns
    .map(
      (col) =>
        `<th style="text-align: ${col.align || 'left'}; width: ${col.width || 'auto'};">${col.label}</th>`
    )
    .join('');

  const bodyRows = rows
    .map(
      (row) =>
        `<tr>${columns
          .map(
            (col) =>
              `<td style="text-align: ${col.align || 'left'};">${formatCellValue(row[col.id], col)}</td>`
          )
          .join('')}</tr>`
    )
    .join('');

  const summaryRows = summary
    ? summary
        .map(
          (item) =>
            `<tr class="summary-row"><td colspan="${columns.length}" style="text-align: right;"><strong>${item.label}:</strong> ${item.value}</td></tr>`
        )
        .join('')
    : '';

  const metadataText = formatReportMetadata(metadata);
  const metadataLines = metadataText.split('\n').map((line) => `<p>${line}</p>`).join('');

  return `
    <div class="report-container">
      <div class="report-header">
        <h1>${metadata.title}</h1>
        ${metadata.description ? `<p class="report-description">${metadata.description}</p>` : ''}
        <div class="report-metadata">
          ${metadataLines}
        </div>
      </div>
      
      <div class="report-content">
        <table class="report-table">
          <thead>
            <tr>${headerRow}</tr>
          </thead>
          <tbody>
            ${bodyRows}
            ${summaryRows}
          </tbody>
        </table>
      </div>
      
      <div class="report-footer">
        <p>Page <span class="page-number"></span> of <span class="page-count"></span></p>
      </div>
    </div>
  `;
};

/**
 * Generate CSV from report data
 */
export const generateReportCSV = (data: ReportData): string => {
  const { metadata, columns, rows, summary } = data;

  const lines: string[] = [];

  // Add metadata as comments
  lines.push(`# ${metadata.title}`);
  if (metadata.description) {
    lines.push(`# ${metadata.description}`);
  }
  lines.push(`# Generated: ${metadata.generatedDate.toLocaleString()}`);
  lines.push(`# Generated By: ${metadata.generatedBy}`);

  if (metadata.filters && Object.keys(metadata.filters).length > 0) {
    const filterStr = Object.entries(metadata.filters)
      .map(([key, value]) => `${key}: ${value}`)
      .join(', ');
    lines.push(`# Filters: ${filterStr}`);
  }

  lines.push(''); // Empty line

  // Add headers
  const headers = columns.map((col) => `"${col.label}"`).join(',');
  lines.push(headers);

  // Add data rows
  rows.forEach((row) => {
    const values = columns
      .map((col) => {
        const value = formatCellValue(row[col.id], col);
        // Escape quotes and wrap in quotes if contains comma or newline
        if (value.includes(',') || value.includes('\n') || value.includes('"')) {
          return `"${value.replace(/"/g, '""')}"`;
        }
        return `"${value}"`;
      })
      .join(',');
    lines.push(values);
  });

  // Add summary
  if (summary) {
    lines.push(''); // Empty line
    summary.forEach((item) => {
      lines.push(`"${item.label}","${item.value}"`);
    });
  }

  return lines.join('\n');
};

